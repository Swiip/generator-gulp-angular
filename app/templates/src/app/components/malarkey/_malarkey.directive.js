/* global malarkey:false */
(function() {
  'use strict';

  angular
    .module('<%= appName %>')
    .directive('acmeMalarkey', acmeMalarkey);

  /** @ngInject */
  function acmeMalarkey() {
    var directive = {
      restrict: 'E',
      scope: {
        extraValues: '=',
      },
      template: '<div></div>',
      link: linkFunc,
      controller: MalarkeyController,
      controllerAs: 'vm'
    };

    return directive;

    function linkFunc(scope, el, attr, vm) {
      var watcher;
      var typist = malarkey(el[0], {
        typeSpeed: 40,
        deleteSpeed: 40,
        pauseDelay: 800,
        loop: true,
        postfix: ' '
      });

      angular.forEach(scope.extraValues, function(value) {
        typist.type(value).pause().delete();
      });

      watcher = scope.$watch('vm.contributors', function(current, original) {
        console.log('watch vm.contributors');
        console.log(current);
        console.log(original);

        angular.forEach(vm.contributors, function(contributor) {
          typist.type(contributor.login).pause().delete();
        });
      });

      scope.$on('$destroy', function () {
        watcher();
      });
    }

  }

  /** @ngInject */
  function MalarkeyController($log, githubApi) {
    var vm = this;

    vm.contributors = [];

    activate();

    function activate() {
      return getContributors().then(function() {
        $log.info('Activated Contributors View');
      });
    }

    function getContributors() {
      return githubApi.getContributors(10).then(function(data) {
          vm.contributors = data;

          return vm.contributors;
        });
    }
  }

})();
